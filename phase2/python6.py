# การ เพิ่ม ข้อมูล append,insert

animal = ["cat","dog","giraff"]

anime =["naruto","one peice","ao no exocist"]
#append (สมาชิก) เพิ่มเข้าไป
print("ก่อนเพิ่ม ",animal)
animal.append("kangaroo")

print("หลังเพิ่ม",animal)

#insert(index,สมาชิก) เพิ่มเข้าไปแทรก
print("ก่อนเพิ่ม",anime)
anime.insert(1,"doraemon")

print("หลังเพิ่ม",anime)